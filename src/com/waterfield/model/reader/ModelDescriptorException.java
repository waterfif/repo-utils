package com.waterfield.model.reader;

public class ModelDescriptorException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -3115795763391325175L;

	public ModelDescriptorException() {
		super();
	}
	
	public ModelDescriptorException(String msg) {
		super(msg);
	}
	
	public ModelDescriptorException(String msg, Throwable thr) {
		super(msg, thr);
	}

}
